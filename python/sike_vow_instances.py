def load_instance(modulus):
    if modulus not in insts_stats:
        keys = insts_stats.keys()
        keys.sort()
        raise ValueError("Only available moduli are %s" % ", ".join(keys))
    
    import vow_sike.sike_vow as sike_vow
    ret = []
    insts = insts_stats[modulus]
    for inst in insts:
        t = sike_vow.instance_t()
        t.MODULUS = inst["MODULUS"]
        t.e = inst["e"]
        t.MEMORY_LOG_SIZE = inst["MEMORY_LOG_SIZE"]
        t.ALPHA = inst["ALPHA"]
        t.BETA = inst["BETA"]
        t.GAMMA = inst["GAMMA"]
        t.PRNG_SEED = inst["PRNG_SEED"]
        t.delta = inst["delta"]
        sike_vow.load_f2elm(t.jinv, inst["jinv"])

        e0 = sike_vow.CurveAndPointsSIDH()
        sike_vow.load_f2elm(e0.a24,inst["E"][0]["a24"])
        sike_vow.load_f2elm(e0.xp, inst["E"][0]["xp"])
        sike_vow.load_f2elm(e0.xq, inst["E"][0]["xq"])
        sike_vow.load_f2elm(e0.xpq,inst["E"][0]["xpq"])
        
        e1 = sike_vow.CurveAndPointsSIDH()
